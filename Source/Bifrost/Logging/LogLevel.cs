namespace Bifrost.Logging
{
    /// <summary>
    /// The severity of a log entry
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// Trace message
        /// </summary>
        Trace,

        /// <summary>
        /// Informational message
        /// </summary>
        Info,

        /// <summary>
        /// Warning message
        /// </summary>
        Warning,

        /// <summary>
        /// Critical message
        /// </summary>
        Critical,

        /// <summary>
        /// Error message
        /// </summary>
        Error
    }
}
